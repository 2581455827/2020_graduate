  /* USER CODE BEGIN 2 */
	//初始化光强
	Init_BH1750();
	//初始化温湿度
	DHT11_Init();
	//开启PM2.5 ADC 
	if(HAL_ADC_Start_DMA(&hadc1,(uint32_t*)&ADC_ConvertedValue[0],12)!=HAL_OK){
		 Error_Handler();
	}
	//开启气体ADC
	HAL_ADC_Start(&hadc2);
